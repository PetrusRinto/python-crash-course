alien_color = ('green', 'yellow', 'red')

# 'if' is True.
alien = alien_color[0]
if 'green' in alien:
    print(f"\n5 points for shooting a {alien} that alien!\n")
elif 'yellow' in alien:
    print(f"\n10 points for shooting a {alien} alien!\n")
else:
    print(f"\n15 points for shooting a {alien_color} alien!\n")

# 'elif' is True.
alien = alien_color[1]
if 'green' in alien:
    print(f"\n5 points for shooting a {alien} that alien!\n")
elif 'yellow' in alien:
    print(f"\n10 points for shooting a {alien} alien!\n")
else:
    print(f"\n15 points for shooting[1] a {alien} alien!\n")

# 'else' is True.
