age = 23

if age < 2:
    print('You are a baby!')
elif age < 4:
    print('You are a toddler!')
elif age < 13:
    print("You're a kid!")
elif age < 20:
    print("You're a teenager!")
elif age < 65:
    print("You're an adult!")
else:
    print("You are an elder!")