def city_country(city, country):
    """Takes in a city and a country value."""
    city_country = f"\n{city.title()}, {country.title()}"
    print(city_country)

city_country('hokkaido', 'japan')
city_country('beijing', 'china')
city_country('verdal', 'norway')
