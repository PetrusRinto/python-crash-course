def show_messages(messages):
    """Prints a simple message from a list."""
    for message in messages:
        print(f"\n{message}")

message_list = [
    "This is a text.",
    "Haha, that's funny!",
    "Is there bedrock before China?"
    ]

show_messages(message_list)